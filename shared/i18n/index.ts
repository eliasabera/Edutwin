const DICTIONARIES: Record<PreferredLanguage, TranslationTree> = {
	en: EN,
	om: OM,
};

const resolvePath = (tree: TranslationTree, path: string): TranslationValue | undefined => {
	return path.split(".").reduce<TranslationTree | TranslationValue | undefined>((current, key) => {
		if (!current || typeof current === "string") {
			return undefined;
		}
		return current[key];
	}, tree) as TranslationValue | undefined;
};

const formatTranslation = (
	value: TranslationValue | undefined,
	params?: Record<string, string | number>,
): string => {
	if (!value) {
		return "";
	}

	if (typeof value === "function") {
		return value(params || {});
	}

	return value;
};

export const getTranslation = (
	language: PreferredLanguage,
	key: string,
	params?: Record<string, string | number>,
) => formatTranslation(resolvePath(DICTIONARIES[language], key), params);

export const useTranslation = () => {
	const language = usePreferredLanguage();

	return useMemo(() => {
		const t = (key: string, params?: Record<string, string | number>) =>
			getTranslation(language, key, params);

		return { language, t };
	}, [language]);
};
